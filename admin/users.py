def ensure_users_table():
    conn = db_conn(False)

    try:
        cur = conn.cursor()

        cur.execute("""
            CREATE TABLE IF NOT EXISTS usuarios (
                id                INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                nome              TEXT,
                email             TEXT,
                cpf               TEXT,
                cpf_digits        TEXT,
                cns               TEXT,
                nascimento        TEXT,
                sexo              TEXT,
                conselho          TEXT,
                registro_conselho TEXT,
                uf_conselho       TEXT,
                cbo               TEXT,
                cbo_descricao     TEXT,
                telefone          TEXT,
                role              TEXT,
                is_active         BOOLEAN DEFAULT TRUE,
                cep               TEXT,
                logradouro        TEXT,
                numero            TEXT,
                complemento       TEXT,
                bairro            TEXT,
                municipio         TEXT,
                uf                TEXT,
                permissoes_json   TEXT,
                senha_hash        TEXT,
                password_hash     TEXT,
                criado_em         TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                atualizado_em     TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            );
        """)

        cols = list_columns(conn, "usuarios")

        def add_col(name: str, ddl: str):
            nonlocal cols
            if name not in cols:
                cur.execute(f"ALTER TABLE usuarios ADD COLUMN IF NOT EXISTS {ddl};")
                cols = list_columns(conn, "usuarios")

        add_col("nome", "nome TEXT")
        add_col("email", "email TEXT")
        add_col("cpf", "cpf TEXT")
        add_col("cpf_digits", "cpf_digits TEXT")
        add_col("cns", "cns TEXT")
        add_col("nascimento", "nascimento TEXT")
        add_col("sexo", "sexo TEXT")
        add_col("conselho", "conselho TEXT")
        add_col("registro_conselho", "registro_conselho TEXT")
        add_col("uf_conselho", "uf_conselho TEXT")
        add_col("cbo", "cbo TEXT")
        add_col("cbo_descricao", "cbo_descricao TEXT")
        add_col("telefone", "telefone TEXT")
        add_col("role", "role TEXT")
        add_col("is_active", "is_active BOOLEAN DEFAULT TRUE")
        add_col("cep", "cep TEXT")
        add_col("logradouro", "logradouro TEXT")
        add_col("numero", "numero TEXT")
        add_col("complemento", "complemento TEXT")
        add_col("bairro", "bairro TEXT")
        add_col("municipio", "municipio TEXT")
        add_col("uf", "uf TEXT")
        add_col("permissoes_json", "permissoes_json TEXT")
        add_col("senha_hash", "senha_hash TEXT")
        add_col("password_hash", "password_hash TEXT")
        add_col("criado_em", "criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP")
        add_col("atualizado_em", "atualizado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP")

        cur.execute("""
            UPDATE usuarios
               SET cpf_digits = REGEXP_REPLACE(COALESCE(cpf, ''), '[^0-9]', '', 'g')
             WHERE cpf IS NOT NULL
               AND (cpf_digits IS NULL OR BTRIM(cpf_digits) = '');
        """)

        cur.execute("""
            UPDATE usuarios
               SET criado_em = CURRENT_TIMESTAMP
             WHERE criado_em IS NULL;
        """)

        cur.execute("""
            UPDATE usuarios
               SET atualizado_em = CURRENT_TIMESTAMP
             WHERE atualizado_em IS NULL;
        """)

        cur.execute("""
            UPDATE usuarios
               SET password_hash = senha_hash
             WHERE (password_hash IS NULL OR BTRIM(password_hash) = '')
               AND (senha_hash IS NOT NULL AND BTRIM(senha_hash) <> '');
        """)

        cur.execute("""
            UPDATE usuarios
               SET senha_hash = password_hash
             WHERE (senha_hash IS NULL OR BTRIM(senha_hash) = '')
               AND (password_hash IS NOT NULL AND BTRIM(password_hash) <> '');
        """)

        # Índices únicos parciais
        cur.execute("""
            CREATE UNIQUE INDEX IF NOT EXISTS idx_usuarios_email
                ON usuarios(email)
                WHERE email IS NOT NULL AND BTRIM(email) <> '';
        """)

        cur.execute("""
            CREATE UNIQUE INDEX IF NOT EXISTS idx_usuarios_cpf
                ON usuarios(cpf)
                WHERE cpf IS NOT NULL AND BTRIM(cpf) <> '';
        """)

        cur.execute("""
            CREATE UNIQUE INDEX IF NOT EXISTS idx_usuarios_cpf_digits
                ON usuarios(cpf_digits)
                WHERE cpf_digits IS NOT NULL AND BTRIM(cpf_digits) <> '';
        """)

        conn.commit()

    except Exception:
        conn.rollback()
        raise

    finally:
        conn.close()


