@admin_bp.route("/usuarios")
@admin_required
def usuarios_listar():
    usuarios = []
    conn = None

    try:
        ensure_users_table()

        conn = db_conn(True)
        cur = conn.cursor()

        mapa_cbo = {}

        try:
            cur.execute("""
                SELECT
                    BTRIM(COALESCE(codigo, '')) AS codigo,
                    BTRIM(COALESCE(descricao, '')) AS descricao
                  FROM cbo_catalogo
                 WHERE BTRIM(COALESCE(codigo, '')) <> '';
            """)

            for r in _fetchall_as_dicts(cur):
                mapa_cbo[(r.get("codigo") or "").strip()] = (r.get("descricao") or "").strip()

        except Exception:
            conn.rollback()
            mapa_cbo = {}

        cur.execute("""
            SELECT
                id,
                nome,
                cpf,
                cpf_digits,
                email,
                role,
                is_active,
                criado_em,
                cbo,
                cbo_descricao
              FROM usuarios
             ORDER BY id DESC;
        """)

        rows = _fetchall_as_dicts(cur)

        for r in rows:
            cbo_codigo = (r.get("cbo") or "").strip()
            cbo_descricao = (r.get("cbo_descricao") or "").strip()

            if not cbo_descricao:
                cbo_descricao = mapa_cbo.get(cbo_codigo, "")

            usuarios.append({
                "id": r.get("id"),
                "nome": r.get("nome"),
                "cpf": r.get("cpf"),
                "cpf_digits": r.get("cpf_digits"),
                "email": r.get("email"),
                "role": r.get("role"),
                "is_active": _normalize_bool(r.get("is_active")),
                "criado_em": r.get("criado_em"),
                "cbo": cbo_codigo,
                "cbo_descricao": cbo_descricao,
                "cbo_label": f"{cbo_codigo} — {cbo_descricao}" if cbo_codigo and cbo_descricao else (cbo_codigo or "—"),
            })

    except Exception as e:
        if conn:
            conn.rollback()
        flash(f"Falha ao carregar usuários: {e}", "error")

    finally:
        if conn:
            conn.close()

    return render_template(
        "admin-usuarios.html",
        usuarios=usuarios,
        niveis=NIVEIS,
        cbos_catalogo=get_cbos_catalogo_local(limit=200),
    )


