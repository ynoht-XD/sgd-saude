def _fetchall_as_dicts(cur):
    rows = cur.fetchall() or []
    out = []

    for row in rows:
        if isinstance(row, dict):
            out.append(dict(row))
            continue

        try:
            out.append(dict(row))
            continue
        except Exception:
            pass

        cols = [c[0] for c in cur.description] if cur.description else []
        out.append(dict(zip(cols, row)))

    return out


def _fetchone_as_dict(cur):
    row = cur.fetchone()

    if row is None:
        return None

    if isinstance(row, dict):
        return dict(row)

    try:
        return dict(row)
    except Exception:
        pass

    cols = [c[0] for c in cur.description] if cur.description else []
    return dict(zip(cols, row))


def _normalize_bool(v) -> bool:
    if isinstance(v, bool):
        return v

    return str(v).strip().lower() in {
        "1", "true", "t", "yes", "sim", "ativo"
    }


def _nz(v):
    v = (v or "").strip()
    return v if v else None


def _integrity_error_class():
    try:
        import psycopg
        return psycopg.IntegrityError
    except Exception:
        pass

    try:
        import psycopg2
        return psycopg2.IntegrityError
    except Exception:
        pass

    return Exception


