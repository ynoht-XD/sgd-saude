def load_user(uid: int):
    conn = db_conn(True)

    try:
        # limpa qualquer transação abortada anterior
        try:
            conn.rollback()
        except Exception:
            pass

        cur = conn.cursor()

        cur.execute("""
            SELECT *
              FROM usuarios
             WHERE id = %s
             LIMIT 1;
        """, (uid,))

        return _fetchone_as_dict(cur)

    except Exception:
        try:
            conn.rollback()
        except Exception:
            pass
        raise

    finally:
        try:
            conn.close()
        except Exception:
            pass

