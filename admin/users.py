def get_cbo_descricao(codigo: str | None) -> str:
    codigo = only_digits(codigo)[:6]

    if not codigo:
        return ""

    conn = db_conn(True)

    try:
        cur = conn.cursor()

        try:
            cur.execute("""
                SELECT BTRIM(COALESCE(descricao, '')) AS descricao
                  FROM cbo_catalogo
                 WHERE BTRIM(COALESCE(codigo, '')) = %s
                 LIMIT 1;
            """, (codigo,))

            row = _fetchone_as_dict(cur)

            if row and (row.get("descricao") or "").strip():
                return row["descricao"].strip()

        except Exception:
            conn.rollback()

        try:
            cur.execute("""
                SELECT BTRIM(COALESCE(no_ocupacao, '')) AS descricao
                  FROM ocupacoes
                 WHERE BTRIM(COALESCE(co_ocupacao, '')) = %s
                 LIMIT 1;
            """, (codigo,))

            row = _fetchone_as_dict(cur)

            if row:
                return (row.get("descricao") or "").strip()

        except Exception:
            conn.rollback()

        return ""

    finally:
        conn.close()


def cbo_existe_no_catalogo(codigo: str | None) -> bool:
    codigo = only_digits(codigo)[:6]

    if not codigo:
        return False

    conn = db_conn(True)

    try:
        cur = conn.cursor()

        try:
            cur.execute("""
                SELECT 1
                  FROM cbo_catalogo
                 WHERE BTRIM(COALESCE(codigo, '')) = %s
                 LIMIT 1;
            """, (codigo,))

            if cur.fetchone():
                return True

        except Exception:
            conn.rollback()

        try:
            cur.execute("""
                SELECT 1
                  FROM ocupacoes
                 WHERE BTRIM(COALESCE(co_ocupacao, '')) = %s
                 LIMIT 1;
            """, (codigo,))

            return cur.fetchone() is not None

        except Exception:
            conn.rollback()
            return False

    finally:
        conn.close()


def get_cbos_catalogo_local(q: str | None = None, limit: int = 200):
    conn = db_conn(True)

    try:
        cur = conn.cursor()
        q_norm = (q or "").strip()
        q_digits = only_digits(q_norm)

        def montar_items(rows):
            return [
                {
                    "id": (r.get("codigo") or "").strip(),
                    "codigo": (r.get("codigo") or "").strip(),
                    "descricao": (r.get("descricao") or "").strip(),
                    "label": f"{(r.get('codigo') or '').strip()} - {(r.get('descricao') or '').strip()}".strip(" -"),
                }
                for r in rows
                if (r.get("codigo") or "").strip()
            ]

        try:
            sql = """
                SELECT
                    BTRIM(COALESCE(codigo, '')) AS codigo,
                    BTRIM(COALESCE(descricao, '')) AS descricao
                  FROM cbo_catalogo
                 WHERE BTRIM(COALESCE(codigo, '')) <> ''
            """
            params = []

            if q_norm:
                sql += """
                   AND (
                        CAST(codigo AS TEXT) ILIKE %s
                        OR COALESCE(descricao, '') ILIKE %s
                   )
                """
                params.extend([f"%{q_digits or q_norm}%", f"%{q_norm}%"])

            sql += """
                 ORDER BY codigo ASC
                 LIMIT %s;
            """
            params.append(int(limit))

            cur.execute(sql, params)
            items = montar_items(_fetchall_as_dicts(cur))

            if items:
                return items

        except Exception:
            conn.rollback()

        try:
            sql = """
                SELECT
                    BTRIM(COALESCE(co_ocupacao, '')) AS codigo,
                    BTRIM(COALESCE(no_ocupacao, '')) AS descricao
                  FROM ocupacoes
                 WHERE BTRIM(COALESCE(co_ocupacao, '')) <> ''
            """
            params = []

            if q_norm:
                sql += """
                   AND (
                        CAST(co_ocupacao AS TEXT) ILIKE %s
                        OR COALESCE(no_ocupacao, '') ILIKE %s
                   )
                """
                params.extend([f"%{q_digits or q_norm}%", f"%{q_norm}%"])

            sql += """
                 ORDER BY codigo ASC
                 LIMIT %s;
            """
            params.append(int(limit))

            cur.execute(sql, params)
            return montar_items(_fetchall_as_dicts(cur))

        except Exception:
            conn.rollback()
            return []

    finally:
        conn.close()


