@procedimentos_bp.route("/lote", methods=["GET", "POST"])
def lote():
    ensure_schema()

    resultados = []
    total_enviados = 0
    total_encontrados = 0

    if request.method == "POST":
        texto = request.form.get("codigos", "") or ""
        codigos = [
            normalize_text(c)
            for c in re.split(r"[\n,; ]+", texto)
            if normalize_text(c)
        ]

        total_enviados = len(codigos)

        if codigos:
            conn = get_conn()

            try:
                cur = conn.cursor()

                for codigo in codigos:
                    cur.execute("""
                        SELECT
                            id,
                            codigo,
                            descricao,
                            complexidade,
                            competencia,
                            valor_sh,
                            valor_sa,
                            valor_sp,
                            valor_total,
                            co_financiamento,
                            no_financiamento,
                            co_rubrica,
                            no_rubrica,
                            qtd_cids,
                            cids_codigos,
                            cids_descricoes,
                            qtd_cbos,
                            cbos_codigos,
                            cbos_descricoes,
                            qtd_servicos,
                            servicos_codigos,
                            servicos_descricoes,
                            classificacoes_codigos,
                            classificacoes_descricoes
                        FROM procedimentos
                        WHERE codigo = %s
                        LIMIT 1
                    """, (codigo,))

                    row = cur.fetchone()

                    if row:
                        cols = [desc[0] for desc in cur.description]
                        item = dict(zip(cols, row))
                        item["encontrado"] = True
                        resultados.append(item)
                        total_encontrados += 1
                    else:
                        resultados.append({
                            "codigo": codigo,
                            "descricao": "Não encontrado",
                            "encontrado": False
                        })

            finally:
                conn.close()

    return render_template(
        "proced_lote.html",
        resultados=resultados,
        total_enviados=total_enviados,
        total_encontrados=total_encontrados
    )

